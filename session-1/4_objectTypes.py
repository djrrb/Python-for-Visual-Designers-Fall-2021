# multiply one integer by another
print(5*10)
# > 50

# five is surrounded by quotes, that makes it a string. If we multiply it by 10, it will just repeat 10 times
print('5'*10)
# 5555555555

# we can multiply any string 10 times
print('hello world '*10)
# > hello world hello world hello world hello world hello world hello world hello world hello world hello world hello world

# if we want to add to a string, it has to be another string.
print('hello world '+'10')
# > 'hello world 10'